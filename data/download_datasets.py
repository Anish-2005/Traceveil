#!/usr/bin/env python3
"""
Download Industry-Standard Fraud Detection Datasets
Automated download and setup for Traceveil training
"""

import os
import sys
import requests
import zipfile
import tarfile
from pathlib import Path
import subprocess
import shutil

# Add project root to path
sys.path.append(os.path.dirname(os.path.dirname(os.path.abspath(__file__))))

DATA_DIR = "data"
RAW_DIR = os.path.join(DATA_DIR, "raw")

os.makedirs(RAW_DIR, exist_ok=True)

class DatasetDownloader:
    """Download industry-standard fraud detection datasets"""

    def __init__(self):
        self.downloaded_datasets = []

    def download_ieee_cis_fraud(self):
        """Download IEEE-CIS Fraud Detection dataset from Kaggle"""
        print("📥 Downloading IEEE-CIS Fraud Detection Dataset...")
        print("This requires Kaggle API credentials.")

        dataset_name = "ieee-fraud-detection"
        output_path = os.path.join(RAW_DIR, "ieee-cis-fraud-detection")

        try:
            # Use kaggle API if available
            import kaggle
            kaggle.api.competition_download_files(dataset_name, path=output_path, quiet=False)

            # Extract if downloaded as zip
            zip_files = list(Path(output_path).glob("*.zip"))
            if zip_files:
                with zipfile.ZipFile(zip_files[0], 'r') as zip_ref:
                    zip_ref.extractall(output_path)

            print("✅ IEEE-CIS dataset downloaded successfully")
            self.downloaded_datasets.append('ieee_cis')
            return True

        except ImportError:
            print("❌ Kaggle API not installed. Please install with: pip install kaggle")
            print("   Then set up your API credentials: https://github.com/Kaggle/kaggle-api#api-credentials")
            self._manual_download_instructions('ieee_cis')
            return False
        except Exception as e:
            print(f"❌ Failed to download IEEE-CIS: {e}")
            self._manual_download_instructions('ieee_cis')
            return False

    def download_credit_card_fraud(self):
        """Download European Credit Card Fraud dataset"""
        print("📥 Downloading Credit Card Fraud Dataset...")

        dataset_name = "mlg-ulb/creditcardfraud"
        output_path = os.path.join(RAW_DIR, "creditcardfraud")

        try:
            import kaggle
            kaggle.api.dataset_download_files(dataset_name, path=output_path, quiet=False)

            # Extract if downloaded as zip
            zip_files = list(Path(output_path).glob("*.zip"))
            if zip_files:
                with zipfile.ZipFile(zip_files[0], 'r') as zip_ref:
                    zip_ref.extractall(output_path)

            print("✅ Credit Card Fraud dataset downloaded successfully")
            self.downloaded_datasets.append('credit_card')
            return True

        except ImportError:
            print("❌ Kaggle API not installed.")
            self._manual_download_instructions('credit_card')
            return False
        except Exception as e:
            print(f"❌ Failed to download Credit Card Fraud: {e}")
            self._manual_download_instructions('credit_card')
            return False

    def download_nab_dataset(self):
        """Download Numenta Anomaly Benchmark (NAB) dataset"""
        print("📥 Downloading NAB Time Series Anomaly Dataset...")

        url = "https://github.com/numenta/NAB/archive/master.zip"
        output_path = os.path.join(RAW_DIR, "NAB")
        zip_path = os.path.join(RAW_DIR, "nab_master.zip")

        try:
            # Download the zip file
            response = requests.get(url, stream=True)
            response.raise_for_status()

            with open(zip_path, 'wb') as f:
                for chunk in response.iter_content(chunk_size=8192):
                    f.write(chunk)

            # Extract
            with zipfile.ZipFile(zip_path, 'r') as zip_ref:
                zip_ref.extractall(RAW_DIR)

            # Move to correct location
            extracted_dir = os.path.join(RAW_DIR, "NAB-master")
            if os.path.exists(extracted_dir):
                if os.path.exists(output_path):
                    shutil.rmtree(output_path)
                os.rename(extracted_dir, output_path)

            # Clean up
            os.remove(zip_path)

            print("✅ NAB dataset downloaded successfully")
            self.downloaded_datasets.append('nab')
            return True

        except Exception as e:
            print(f"❌ Failed to download NAB: {e}")
            self._manual_download_instructions('nab')
            return False

    def generate_synthetic_datasets(self):
        """Generate synthetic datasets for cases where real data isn't available"""
        print("🔧 Generating synthetic datasets...")

        # Exam behavior data will be generated by the loader
        # Fraud network data will be generated by the loader

        print("✅ Synthetic datasets ready for generation")
        self.downloaded_datasets.extend(['exam_behavior', 'fraud_network'])

    def _manual_download_instructions(self, dataset_name: str):
        """Provide manual download instructions"""
        instructions = {
            'ieee_cis': """
            IEEE-CIS Fraud Detection Dataset:
            1. Go to: https://www.kaggle.com/c/ieee-fraud-detection
            2. Download the following files:
               - train_transaction.csv
               - train_identity.csv
               - test_transaction.csv
               - test_identity.csv
            3. Create folder: data/raw/ieee-cis-fraud-detection/
            4. Place files in that folder
            """,

            'credit_card': """
            Credit Card Fraud Dataset:
            1. Go to: https://www.kaggle.com/mlg-ulb/creditcardfraud
            2. Download creditcard.csv
            3. Create folder: data/raw/creditcardfraud/
            4. Place the file in that folder
            """,

            'nab': """
            NAB Time Series Dataset:
            1. Go to: https://github.com/numenta/NAB
            2. Download as ZIP
            3. Extract to: data/raw/NAB/
            """
        }

        print(f"\n📋 MANUAL DOWNLOAD REQUIRED for {dataset_name.upper()}:")
        print(instructions.get(dataset_name, "Please check the dataset source for download instructions."))

    def setup_kaggle_credentials(self):
        """Help set up Kaggle API credentials"""
        print("\n🔑 Setting up Kaggle API credentials...")

        kaggle_dir = os.path.expanduser("~/.kaggle")
        kaggle_json = os.path.join(kaggle_dir, "kaggle.json")

        if os.path.exists(kaggle_json):
            print("✅ Kaggle credentials already exist")
            return True

        print("Kaggle credentials not found.")
        print("Please follow these steps:")
        print("1. Go to https://www.kaggle.com/account")
        print("2. Click 'Create New API Token'")
        print("3. Save the downloaded kaggle.json file")
        print(f"4. Move it to: {kaggle_json}")
        print("5. Run: chmod 600 ~/.kaggle/kaggle.json (Linux/Mac)")

        return False

    def download_all_datasets(self):
        """Download all available industry datasets"""
        print("🚀 Starting Industry Dataset Download for Traceveil")
        print("=" * 60)

        # Check Kaggle setup
        kaggle_available = self.setup_kaggle_credentials()

        if kaggle_available:
            # Download real datasets
            self.download_ieee_cis_fraud()
            self.download_credit_card_fraud()
        else:
            print("⚠️ Kaggle API not available. Will use synthetic data where needed.")

        # Download NAB (doesn't require Kaggle)
        self.download_nab_dataset()

        # Generate synthetic datasets
        self.generate_synthetic_datasets()

        # Summary
        print("\n📊 DOWNLOAD SUMMARY")
        print("=" * 60)
        print(f"Datasets ready: {', '.join(self.downloaded_datasets)}")

        if 'ieee_cis' in self.downloaded_datasets:
            print("✅ IEEE-CIS Fraud Detection: REAL DATA")
        else:
            print("❌ IEEE-CIS Fraud Detection: MANUAL DOWNLOAD REQUIRED")

        if 'credit_card' in self.downloaded_datasets:
            print("✅ Credit Card Fraud: REAL DATA")
        else:
            print("❌ Credit Card Fraud: MANUAL DOWNLOAD REQUIRED")

        if 'nab' in self.downloaded_datasets:
            print("✅ NAB Time Series: REAL DATA")
        else:
            print("❌ NAB Time Series: MANUAL DOWNLOAD REQUIRED")

        print("✅ Exam Behavior: SYNTHETIC (REALISTIC)")
        print("✅ Fraud Network: SYNTHETIC (GRAPH ML READY)")

        print("\n🎯 Next Steps:")
        print("1. Run: python data/train_industry_datasets.py")
        print("2. Models will be trained on industry-standard data")
        print("3. Traceveil becomes production-ready!")

        return self.downloaded_datasets

def main():
    downloader = DatasetDownloader()
    downloader.download_all_datasets()

if __name__ == "__main__":
    main()